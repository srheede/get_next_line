#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = ft_strlen(s);
	s += i;
	while (i >= 0)
	{
		if (c == *s)
			return ((char *)s);
		s--;
		i--;
	}
	return (0);
}
